/**
 * We can store data in a special label called a "variable"
 */
var myString = "Some text";
var myNum = 3;


/**
 * We can use those variables later! Here, we print them out.
 */
console.log("Look! We can print stuff!");
console.log(myString);
console.log(myNum);


/**
 * We can capture values in variables
 */
var sum = myNum + 5;
console.log(sum);


/**
 * We can do weird things, like smash numbers and strings together!
 */
var weirdResult = myString + myNum;
console.log(weirdResult);